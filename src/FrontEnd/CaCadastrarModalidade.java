    public CaCadastrarModalidade(int Codigo) {
        initComponents();
        this.c = new ConexaoMySQL();
        this.setModal(true);
        Cod= Codigo;
        this.setTitle("Cadastrando Modalidade");
        
        
        if(Codigo == 0){
            this.setTitle("Cadastrando Modalidades");

        }
        else{
            this.setTitle("Editando Modalidades");

            try{
                String SQL="select * From modalidade where CodModalidade = "+Codigo;
                this.c.setResultSet(SQL);
                this.c.getResultSet().next();

                txtNome.setText(this.c.getResultSet().getString("nome"));

            }
            catch(SQLException e){
                JOptionPane.showMessageDialog(this, e);
            }
        }
        
        
    }

