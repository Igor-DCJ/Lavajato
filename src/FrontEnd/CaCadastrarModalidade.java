    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed

        // Toda essa sequencia de Strings seguido de Ifs é para verificar se os campos foram preenchidos

        String Nome=txtNome.getText();
        int TamanhoNome=Nome.length();

        if(TamanhoNome>=1){

            if(Cod==0){
                String SQL="Insert into modalidade(Nome)"
                + " Values('" + txtNome.getText() + "');";
                //System.out.print(SQL);
                this.c.SQLExecute(SQL);
                this.dispose();
                JOptionPane.showMessageDialog(this, "Cadastrado com sucesso");
            }
            else{
                String SQL= "Update modalidade set Nome = '"+txtNome.getText()+"' where( CodModalidade = '"+Cod+"' )";
                //System.out.print(SQL);
                this.c.SQLExecute(SQL);
                this.dispose();
                JOptionPane.showMessageDialog(this, "Editado com sucesso");
            }

        }
        else{
            JOptionPane.showMessageDialog(this, "O Nome está muito pequeno.");
        }

    }//GEN-LAST:event_btnSalvarActionPerformed

